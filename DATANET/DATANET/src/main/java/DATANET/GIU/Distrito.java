    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        Conexion objconexion = new Conexion();
        
        distritoBL oDistritos= recuperarDatosGIU();
        
        String strSentenciaInsert = String.format("INSERT INTO DISTRITO (Cod_Distrito, Nom_Distrito) "
                + "VALUES('%s','%s')",oDistritos.getCod_distrito(),oDistritos.getNom_distrito());
        objconexion.ejecutarSentenciaSQL(strSentenciaInsert);
        this.mostrardatos();
        this.limpiar();
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void tblDistritoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDistritoMouseClicked
        // TODO add your handling code here:
        
        if(evt.getClickCount()==1){
            JTable receptor = (JTable) evt.getSource();
            txtCod_Distrito.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),0).toString());
            txtNom_Distrito.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),1).toString());
        }
        btnAgregar.setEnabled(false);
        btnModificar.setEnabled(true);
        btnEliminar.setEnabled(true);
    }//GEN-LAST:event_tblDistritoMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Conexion objconexion = new Conexion();
        distritoBL oDistritos= recuperarDatosGIU();
        String strSentenciaInsert = String.format("DELETE FROM DISTRITO WHERE Cod_Distrito = '%s'",oDistritos.getCod_distrito());
        objconexion.ejecutarSentenciaSQL(strSentenciaInsert);
        this.mostrardatos();
        this.limpiar();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        Conexion objconexion = new Conexion();
        
        distritoBL oDistritos= recuperarDatosGIU();
        
        String strSentenciaInsert = String.format("UPDATE DISTRITO SET Nom_Distrito='%s' "
                + "WHERE Cod_Distrito = '%s'",oDistritos.getNom_distrito(),oDistritos.getCod_distrito());
        objconexion.ejecutarSentenciaSQL(strSentenciaInsert);
        this.mostrardatos();
        this.limpiar();
    }//GEN-LAST:event_btnModificarActionPerformed
    
    public void mostrardatos(){
        
        while (mode1.getRowCount()>0) {
            mode1.removeRow(0);
        }
        
        Conexion objconexion = new Conexion();
        try {
            ResultSet resultado = objconexion.consultarRegistros("SELECT * FROM DISTRITO");
            while (resultado.next()) {
                System.out.println(resultado.getString("Cod_Distrito"));
                System.out.println(resultado.getString("Nom_Distrito"));
                Object[] oDistrito = {resultado.getString("Cod_Distrito"),resultado.getString("Nom_Distrito")};
                mode1.addRow(oDistrito);
            }
        } catch (Exception e) {
                System.out.print(e);
        }
    }
    
    public distritoBL recuperarDatosGIU() {
        distritoBL oDistritos = new distritoBL();
        
        oDistritos.setCod_distrito(txtCod_Distrito.getText());
        oDistritos.setNom_distrito(txtNom_Distrito.getText());

        return oDistritos;
    }
    
    public void limpiar(){
        txtCod_Distrito.setText("");
        txtNom_Distrito.setText("");
        
        btnAgregar.setEnabled(true);
        btnModificar.setEnabled(false);
        btnEliminar.setEnabled(false);
    }
    
