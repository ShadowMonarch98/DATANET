    public void mostrardatos(){
        
        while (mode1.getRowCount()>0) {
            mode1.removeRow(0);
        }
        
        Conexion objconexion = new Conexion();
        try {
            ResultSet resultado = objconexion.consultarRegistros("SELECT * FROM DISTRITO");
            while (resultado.next()) {
                System.out.println(resultado.getString("Cod_Distrito"));
                System.out.println(resultado.getString("Nom_Distrito"));
                Object[] oDistrito = {resultado.getString("Cod_Distrito"),resultado.getString("Nom_Distrito")};
                mode1.addRow(oDistrito);
            }
        } catch (Exception e) {
                System.out.print(e);
        }
    }
    
    public distritoBL recuperarDatosGIU() {
        distritoBL oDistritos = new distritoBL();
        
        oDistritos.setCod_distrito(txtCod_Distrito.getText());
        oDistritos.setNom_distrito(txtNom_Distrito.getText());

        return oDistritos;
    }
    
    public void limpiar(){
        txtCod_Distrito.setText("");
        txtNom_Distrito.setText("");
        
        btnAgregar.setEnabled(true);
        btnModificar.setEnabled(false);
        btnEliminar.setEnabled(false);
    }
    
